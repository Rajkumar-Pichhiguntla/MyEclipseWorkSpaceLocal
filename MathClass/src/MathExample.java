
public class MathExample {
	public static void main(String[] args) {
		double num=90;
		System.out.println(Math.toRadians(num));
		System.out.println((int)(Math.random()*100)+1);
		System.out.println((Math.random()+100));
		
		System.out.println(Math.pow(-3, 3));
		System.out.println(Math.IEEEremainder(10.5, 3));
		System.out.println(Math.floorMod(-9, 4));
		System.out.println(Math.multiplyExact(10000, 10000));
		System.out.println(Math.negateExact(Integer.MIN_VALUE));
	
	}

}
