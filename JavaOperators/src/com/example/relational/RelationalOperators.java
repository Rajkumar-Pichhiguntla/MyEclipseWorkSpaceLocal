package com.example.relational;

public class RelationalOperators {
	public static void main(String[] args) {
		int num1=20;
		float num2=10.5f;
		
		boolean isEquals = (num1==num2);
		
		System.out.println(isEquals);
		
		boolean isGreaterThan = num1>num2;
		
		System.out.println(isGreaterThan);
		
		boolean isLessThan = num1<num2;
		
		System.out.println(isLessThan);
		
		boolean isGreaterThanEquals = num1>=num2;
		
		System.out.println(isGreaterThanEquals);
		
		boolean isLessThanEquals = num1<=num2;
		
		System.out.println(isLessThanEquals);
		
		boolean notEquals = num1!=num2;
		
		System.out.println(notEquals);
		
		
	}

}
